import { mkdir, writeFile } from "node:fs/promises"
import { dirname, resolve } from "node:path"
import { fileURLToPath } from "node:url"

import { build } from "esbuild"

const scriptPath = fileURLToPath(import.meta.url)
const projectRoot = process.cwd()

const header = `/*
LayerManager X — generated bundle
Source project: obsidian-excalidraw-layer-manager
Do not edit this file directly. Edit src/* and run npm run build.
*/`

export const buildLayerManagerBundle = async ({ projectRootOverride = projectRoot } = {}) => {
  const entryFile = resolve(projectRootOverride, "src/main.ts")
  const outputFile = resolve(projectRootOverride, "dist/LayerManager.md")

  const result = await build({
    entryPoints: [entryFile],
    bundle: true,
    write: false,
    platform: "browser",
    format: "iife",
    target: ["es2022"],
    legalComments: "none",
    sourcemap: false,
    charset: "utf8",
    logLevel: "info",
  })

  const bundled = result.outputFiles[0]?.text
  if (!bundled) {
    throw new Error("esbuild did not produce output.")
  }

  await mkdir(dirname(outputFile), { recursive: true })
  await writeFile(outputFile, `${header}\n${bundled}\n`, "utf8")

  console.log(`[build] Wrote ${outputFile}`)
  return { entryFile, outputFile }
}

if (process.argv[1] && resolve(process.argv[1]) === scriptPath) {
  await buildLayerManagerBundle()
}
